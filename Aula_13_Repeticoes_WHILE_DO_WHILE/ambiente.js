/* ESTRUTURA COM TESTE NO INÍCIO*/
console.log("WHILE TESTE NO INÍCIO");

var contador = 1;
while (contador <= 6) {
  console.log(`Passo ${contador}`);
  contador++; //c=c+1
}

console.log("_______________________________________");
console.log(" ");

/* ESTRUTURA COM TESTE NO FINAL*/
console.log("DO WHILE TESTE NO FINAL");
var contador = 1;
do {
  console.log(`Passo ${contador}`);
  contador++;
} while (contador <= 6);

console.log("_______________________________________");
console.log(" ");
